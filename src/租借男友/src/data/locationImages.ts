/** 根据当前地点和时间段返回对应的背景图片 URL
 *  6:00-18:59 为白天，19:00-5:59 为夜晚
 */
export function getLocationBackground(location: string, hour: number): string | undefined {
  // 6:00-18:59 为白天，19:00-5:59 为夜晚
  const timeSuffix = (hour >= 6 && hour < 19) ? '白日' : '夜晚';
  const exactKey = `${location}${timeSuffix}`;
  if (LOCATION_BACKGROUNDS[exactKey]) {
    return LOCATION_BACKGROUNDS[exactKey];
  }

  // 再尝试仅匹配地点（无时段区分的图片）
  if (LOCATION_BACKGROUNDS[location]) {
    return LOCATION_BACKGROUNDS[location];
  }

  return undefined;
}

// ── 预加载相关工具 ──

/** 已预加载的图片 URL 集合（防重复） */
const preloadedUrls = new Set<string>();

/** 预加载单张图片 */
function preloadImage(url: string): void {
  if (preloadedUrls.has(url)) return;
  preloadedUrls.add(url);
  const img = new Image();
  img.src = url;
}

/** 从剧本正文中提取所有地点名称（用于预加载） */
export function extractLocationsFromScript(scriptText: string): string[] {
  const locations = new Set<string>();
  // 匹配 "【地点：xxx】" 或 "地点：xxx" 格式
  const matches = scriptText.match(/【?地点[：:]([^【】\n]+)】?/g);
  if (matches) {
    for (const match of matches) {
      const loc = match.replace(/[【】]/g, '').replace(/地点[：:]/, '').trim();
      if (loc) locations.add(loc);
    }
  }
  return Array.from(locations);
}

/** 预加载指定地点在当前时间和相反时间的背景图片 */
export function preloadLocationBackgrounds(location: string, hour: number): void {
  const currentSuffix = (hour >= 6 && hour < 19) ? '白日' : '夜晚';
  const oppositeSuffix = (hour >= 6 && hour < 19) ? '夜晚' : '白日';

  // 当前时间
  const currentUrl = LOCATION_BACKGROUNDS[`${location}${currentSuffix}`] || LOCATION_BACKGROUNDS[location];
  if (currentUrl) preloadImage(currentUrl);

  // 相反时间（防时间跳转）
  const oppositeUrl = LOCATION_BACKGROUNDS[`${location}${oppositeSuffix}`];
  if (oppositeUrl) preloadImage(oppositeUrl);
}

/** 预加载剧本中所有涉及地点的背景图片 */
export function preloadScriptBackgrounds(scriptText: string, hour: number): void {
  const locations = extractLocationsFromScript(scriptText);
  for (const loc of locations) {
    preloadLocationBackgrounds(loc, hour);
  }
}
